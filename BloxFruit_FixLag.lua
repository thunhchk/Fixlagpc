-- Tạo GUI chính
local ScreenGui = Instance.new("ScreenGui")
local Frame = Instance.new("Frame")
local Title = Instance.new("TextLabel")
local LowButton = Instance.new("TextButton")
local MediumButton = Instance.new("TextButton")
local HighButton = Instance.new("TextButton")
local UltraButton = Instance.new("TextButton")

ScreenGui.Parent = game.Players.LocalPlayer:WaitForChild("PlayerGui")
Frame.Parent = ScreenGui
Frame.Size = UDim2.new(0, 300, 0, 200)
Frame.Position = UDim2.new(0.5, -150, 0.5, -100)
Frame.BackgroundColor3 = Color3.fromRGB(30, 30, 30)

Title.Parent = Frame
Title.Size = UDim2.new(1, 0, 0, 40)
Title.Text = "⚡ Fix Lag Menu ⚡"
Title.TextColor3 = Color3.fromRGB(255, 255, 255)
Title.BackgroundColor3 = Color3.fromRGB(50, 50, 50)

-- Các nút chọn chế độ
local function setupButton(btn, text, y)
    btn.Parent = Frame
    btn.Size = UDim2.new(1, -20, 0, 30)
    btn.Position = UDim2.new(0, 10, 0, y)
    btn.Text = text
    btn.BackgroundColor3 = Color3.fromRGB(70, 70, 70)
    btn.TextColor3 = Color3.fromRGB(255, 255, 255)
end

setupButton(LowButton, "Low", 50)
setupButton(MediumButton, "Medium", 90)
setupButton(HighButton, "High", 130)
setupButton(UltraButton, "Ultra", 170)

-- Hàm fix lag cơ bản
local function basicFix()
    settings().Rendering.QualityLevel = Enum.QualityLevel.Level01
    game.Lighting.GlobalShadows = false
    game.Lighting.FogEnd = 9e9
    game.Lighting.Brightness = 2
    for _, obj in pairs(workspace:GetDescendants()) do
        if obj:IsA("ParticleEmitter") or obj:IsA("Trail") or obj:IsA("Smoke") or obj:IsA("Fire") then
            obj:Destroy()
        end
    end
end

-- Hàm xóa texture/decal
local function removeTextures()
    for _, obj in pairs(workspace:GetDescendants()) do
        if obj:IsA("Decal") or obj:IsA("Texture") then
            obj:Destroy()
        end
    end
end

-- Hàm ẩn player khác (Ultra)
local function hidePlayers()
    for _, plr in pairs(game.Players:GetPlayers()) do
        if plr ~= game.Players.LocalPlayer and plr.Character then
            plr.Character:Destroy()
        end
    end
end

-- Chế độ Low
LowButton.MouseButton1Click:Connect(function()
    basicFix()
    print("✅ Fix Lag Low Mode")
end)

-- Chế độ Medium
MediumButton.MouseButton1Click:Connect(function()
    basicFix()
    removeTextures()
    print("✅ Fix Lag Medium Mode")
end)

-- Chế độ High
HighButton.MouseButton1Click:Connect(function()
    basicFix()
    removeTextures()
    setfpscap(30)
    print("✅ Fix Lag High Mode")
end)

-- Chế độ Ultra
UltraButton.MouseButton1Click:Connect(function()
    basicFix()
    removeTextures()
    hidePlayers()
    setfpscap(20)
    print("✅ Fix Lag Ultra Mode")
end)

